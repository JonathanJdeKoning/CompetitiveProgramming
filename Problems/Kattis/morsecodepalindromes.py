string = input()
newstring = ""
for c in string:
    if c.isalnum():
        newstring+=c

morsestring = ""

for c in newstring:
    morsestring += morse[c.upper()]

if morsestring == "":
    print(0)
else:
    if morsestring == morsestring[::-1]:
        print(1)
    else:
        print(0)
